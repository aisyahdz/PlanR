class TargetViewController: UITableViewController {

    
    var taskList = [String]()
    
    override func viewDidLoad() {
        super.viewDidLoad()
        taskList = ["Makan", "Mandi", "Tidur"]
        self.tableView.register(TaskTableViewCell.self, forCellReuseIdentifier: "taskTableViewCell")
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return taskList.count
    }
    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = self.tableView.dequeueReusableCell(withIdentifier: "taskTableViewCell") as! TaskTableViewCell
        cell.task = taskList[indexPath.row]
//        cell.backgroundColor = UIColor.green
        return cell
    }

